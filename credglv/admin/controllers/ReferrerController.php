	}

	public function __init() {
		global $post;
		credglv()->hook->listenFilter( 'manage_credglv_referrer_posts_columns', [ $this, 'referrerColumns' ] );
		credglv()->hook->listenHook( 'manage_credglv_referrer_posts_custom_column', [ $this, 'renderCustomColumns' ] );
		credglv()->hook->listenFilter( 'post_row_actions', [ $this, 'removeReferrerActions' ] );
		if ( get_post_type() != 'credglv_referrer' ) {
			return;
		}
		parent::init(); // TODO: Change the autogenerated stub
		$this->referrer = $this->getId();
	}

	public function delete() {
		if ( credglv()->wp->wp_delete_post( $_POST['post_id'], true ) ) {
			$this->responseJson( [
				'code'    => 200,
				'message' => 'Delete success',
			] );
		}
	}

	public function referrerColumns( $columns ) {
		unset( $columns['date'] );
		$columns         = array_merge( $columns, $this->customColumns );
		$columns['date'] = 'Modified';

		return $columns;
	}

	/**
	 * Show custom columns content
	 *
	 * @param $column
	 * @param $post_id
	 */
	public function renderCustomColumns( $column, $post_id = '' ) {
		global $post;
		$referrer = ReferrerModel::findOne( $post );

		if ( in_array( $column, array_keys( $this->customColumns ) ) ) {
			switch ( $column ) {
				case 'total' :
					echo credglv()->helpers->general->currencyFormat( $referrer->total_price );
					break;
				case 'sale' :
					echo credglv()->helpers->general->currencyFormat( $referrer->credglv_sale_price );
					break;
				case 'post_date' :
					echo $post->post_date;
					break;
			}
		}
	}

