	public function saveReferrer( $post_id ) {
		$data_post = isset( $_POST ) ? $_POST : array();
		if ( isset( $data_post['action'] ) && $data_post['action'] == 'editpost' ) {
			$this->saveMetaBox( $post_id, $data_post );
		}
	}

	public function saveMetaBox( $post_id, $data_post ) {
		$model = ReferrerModel::getInstance();
		$model->saveMetaData( $post_id, $data_post );
	}

	public function addItem() {

		$referrer_id   = $_POST['referrer_id'];
		$total_price = $_POST['total_price'];
		$course_id   = $_POST['course_id'];
		add_post_meta( $referrer_id, $this->course_meta, $course_id, false );
		if ( empty( get_post_meta( $referrer_id, $this->referrer_total_price ) ) ) {
			add_post_meta( $referrer_id, $this->referrer_total_price, $total_price, true );
		} else {
			update_post_meta( $referrer_id, $this->referrer_total_price, $total_price );

		}
		$this->responseJson( [
			'data'       => '1',
			'totla'      => $total_price,
			'totla_meta' => get_post_meta( $referrer_id, $this->referrer_total_price )
		] );
		exit;
	}

	public function removeItem() {
		$referrer_id   = $_POST['referrer_id'];
		$course_id   = $_POST['course_id'];
		$total_price = $_POST['total_price'];
		if ( $course_id && $referrer_id ) {
			global $wpdb;
//			$sql = 'delete from %1$s where post_id = %2$s and meta_key like %3$s and meta_value = %4$s';
//			$sql = $wpdb->prepare( $sql, $wpdb->postmeta, $referrer_id, $this->course_meta, $course_id );
			$res = get_post_meta( $referrer_id, $this->course_meta );
			delete_post_meta( $referrer_id, $this->course_meta, $course_id );
			if ( empty( get_post_meta( $referrer_id, $this->referrer_total_price ) ) ) {
				add_post_meta( $referrer_id, $this->referrer_total_price, $total_price, true );
			} else {
				update_post_meta( $referrer_id, $this->referrer_total_price, $total_price );
			}
		}
		$this->responseJson( [ 'data' => $res ] );

		exit;
	}

